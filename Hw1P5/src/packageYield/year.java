package packageYield;

public class year {
    double percent;
    int years;
    int initialInvestment;
    double futureValue;
    public year(double percent, int years, int initialInvestment) {
        this.percent = percent;
        this.years = years;
        this.initialInvestment = initialInvestment;
        futureValue = 0.0;
    }
}